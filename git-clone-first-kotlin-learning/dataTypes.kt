fun main()
{
//    val & var keywords are used to declare variables in kotlin
//    var is used for creating mutable variable
//    val is used for creating immutable variables

//    in kotlin we have following data types

    var myName : Any ;

    myName = 1 ;
    println(myName);

    myName = "Allsabah" ;
    println(myName);

    myName = 94.6 ;
    println(myName);

}